export function deathDate(birthday, isMale) {
    var remainingYears;
    if (isMale) {
        remainingYears = timeLeft(menLife, birthday);
    } else {
        remainingYears = timeLeft(womenLife, birthday);
    }
    var millisLeft = remainingYears * 365.25 * 24 * 60 * 60 * 1000
    return new Date(millisLeft + Date.now());
}

export function percentComplete(birthday, isMale) {
    var totalMillis = deathDate(birthday, isMale) - birthday;
    var now = new Date();
    var elapsedMillis = now - birthday;
    return Math.round((elapsedMillis / totalMillis) * 100);
}

export function timeLeft(lifeObj, birthday) {
    var age = yearsDifference(birthday, new Date());
    var whole = Math.floor(age)
    var fraction = age - whole
    var upper = lifeObj[whole.toString()]
    var lower = lifeObj[(whole+1).toString()]
    return ((upper-lower) / fraction) + lower;
}

export function yearsDifference(date1, date2) {
    return Math.abs(date1-date2) / (1000 * 60 * 60 * 24 * 365.25);
}

export function timeRemaining(birthday, isMale) {
    var second = 1000;
    var minute = second * 60;
    var hour = minute * 60;
    var day = hour * 24;
    var year = day * 365;
    var remaining = deathDate(birthday, isMale) - (new Date());
    var yearsLeft = Math.floor(remaining / year);
    remaining = remaining - (yearsLeft * year);
    var daysLeft = Math.floor(remaining / day);
    remaining = remaining - (daysLeft * day);
    var hoursLeft = Math.floor(remaining / hour);
    remaining = remaining - (hoursLeft * hour);
    var minutesLeft = Math.floor(remaining / minute);
    remaining = remaining - (minutesLeft * minute);
    var secondsLeft = remaining / second;
    return {
        years: yearsLeft,
        days: daysLeft,
        hours: hoursLeft,
        minutes: minutesLeft,
        seconds: secondsLeft
    }
}